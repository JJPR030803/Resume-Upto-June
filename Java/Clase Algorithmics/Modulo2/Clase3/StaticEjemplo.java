public class StaticEjemplo {
    public String mensaje = "Hello World";
    public void displayMensaje(){
        System.out.println("Mensaje ="+mensaje);
    }
    public static String saludos = "Buenos Dias";
    public static void displaySaludos(){
        System.out.println("Saludos="+saludos);
    }
}
